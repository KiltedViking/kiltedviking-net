				<h3>Namnet (Kilted Viking)</h3>

				<p>Namnet <b>Kilted Viking</b> myntades av min kusin och referrerar till
					mitt svensk-skottska ursprung och det faktum att jag äger en kilt. 
					Och eftersom jag var i behov av ett nytt ställe att publisera
					min webbplats (då jag slutade hos min arbetsgivare där jag hade
					min webbplats) så bestämde jag mig för att registrera kiltedviking.net
					- en &quot;internationell&quot; adress.</p>
					
				<img src="../images/AnnanBeach.jpg" alt="Annan beach" title="Annan beach" 
					class="img-responsive center-block" />

				<h3>Designen</h3>

				<p>Multimedia är inte min starka sida... så jag brukar försöka göra
					min webbplats så enkel som möjligt. Men samtidigt så använder
					jag denna webbplats för att lära mig mer om webbutveckling samt
					visa mina kunskaper.</p>

				<p>Jag brukar redigera mina webbsidor som text - numera i <b>Visual
					Studio Code</b> av Microsoft (men tidigare har jag använt <strong>Notepad++</strong>
					och <strong>Crimson Editor</strong>) - för att inte förstöra design 
					(vilken design kan ni fråga? :-)). Webbsidorna publicerar jag
					först på min egen dator med Bitnami WAMP, för att testa i Microsoft
					Edge (samt ibland även i Mozilla Firefox och Google Chrome),
					och sen hos mitt webbhotell.</p>
					
				<p>Initialt hade jag stor hjälp av <strong>Web Design Group:s</strong> 
					webbplats och referensdokument, så ett stort tack till dem! Numera
					tar jag ofta hjälp av <strong>Mozila Developer Network</strong> (MDN), 
					så ytterligare ett stort tack till alla som bidrar till informationen! 
					I övrigt så brukar jag &quot;stjäla&quot; idéer jag tycker om från
					andra webbplatser jag tycker om. 25+ år av erfarenhet gör också
					sitt. ;-)</p>

				<img src="../images/Arran.jpg" alt="Arran island" title="Arran island" 
					class="img-responsive center-block" />

				<h3>The technologies used</h3>

				<p>I mainly use HTML, (simple) JavaScript, and CSS (i.e. client-side technologies
					present in most web browsers) in my home pages. Since I bought some space at 
					a web hotel supporting PHP (i.e. server-side technologies), some 
					of the HTML pages have been converted to PHP pages (which, in reality, only 
					means changing the file's extension and slightly changing the code in the 
					page). I also started using FavIcon (as the web hotel supports it - see below).</p>

				<img src="../images/CulzeanCastle.jpg" alt="Culzean castle" title="Culzean castle" 
					class="img-responsive center-block"/>

				<h3>Logo and FavIcon</h3>

				<p>My logo (top left of page) is based on a photo of a <a href="../images/RosemountSun02.jpg">sunset</a>
					(behind my aunt and uncles house, and which I took with my instamatic
					camera back in 2002, i.e. it's scanned). On my Swedish pages I use a photo of
					<a href="../images/HouseOutdoor1.JPG">my old house</a> as a logo
					(which I took with my first digital camera). (People nagged
					me to change it to a photo of my caravan, where I used to live. And
					so I have on my travel pages. :-)</p>

				<p>A FavIcon, or Favorite Icon, is the small icon in the Address
					field of your browser and in browser's bookmarks/favorites. My
					FavIcon is based on my logo (om English pages, i.e. the sunset)
					and I used <strong>HTML-Kit's</strong> online service to create mine.</p>

				<p class="instructions">(See &lt;link&gt; tag with attribute containing
					&quot;favicon.ico&quot; in the code for this page to see how to
					insert a FavIcon in your own page, should your server not do it for you.)</p>
