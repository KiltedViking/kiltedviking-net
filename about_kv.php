				<h3>The name (Kilted Viking)</h3>

				<p>The name <b>Kilted Viking</b> was coined by a cousin and refers to
					my Swedish-Scottish ancestory and me owning a kilt. Needing a web server
					to place my home pages on (as I quit my job where I hosted my site then), 
					I decided to register kiltedviking.net.</p>
					
				<img src="images/AnnanBeach.jpg" alt="Annan beach" title="Annan beach" 
					class="img-responsive center-block" />

				<h3>The design</h3>

				<p>Multimedia has never been my thing... so I usually try to make
					my web sites as simple as possible. This site consists of a bunch
					of home pages with 5 images (logo and four corners in all home pages,
					not counting photos from my travels, etc. :-). The home pages are
					usually edited as text (nowadays in <strong>Notepad++</strong>, but previously 
					in <strong>Crimson Editor</strong>), as to
					not destroy any design features (what design you might ask? :-)
					and to make sure they work as I want them to. My home pages are then viewed
					(tested) in <strong>Mozilla Firefox</strong> (and, if I remember/can be
					bothered, M$ Internet Explorer). The <strong>Web Design Group's</strong> 
					web site and reference documents have been a great help to me (for many years). A big
					thanks goes to them!</p>

				<p>How do I come up with my designs then? I usually steal ideas
					from home pages I visit. :-) Sometimes I manage to make these ideas
					work in my own pages and sometimes I don't. I have also spent the
					last 11-12 years as a webmaster (editing, <strong>not</strong> designing
					the sites! :-), giving me some experience.</p>
				
				<img src="images/Arran.jpg" alt="Arran island" title="Arran island" 
					class="img-responsive center-block" />

				<h3>The technologies used</h3>

				<p>I mainly use HTML, (simple) JavaScript, and CSS (i.e. client-side technologies
					present in most web browsers) in my home pages. Since I bought some space at 
					a web hotel supporting PHP (i.e. server-side technologies), some 
					of the HTML pages have been converted to PHP pages (which, in reality, only 
					means changing the file's extension and slightly changing the code in the 
					page). I also started using FavIcon (as the web hotel supports it - see below).</p>

				<img src="images/CulzeanCastle.jpg" alt="Culzean castle" title="Culzean castle" 
					class="img-responsive center-block"/>

				<h3>Logo and FavIcon</h3>

				<p>My logo (top left of page) is based on a photo of a <a href="images/RosemountSun02.jpg">sunset</a>
					(behind my aunt and uncles house, and which I took with my instamatic
					camera back in 2002, i.e. it's scanned). On my Swedish pages I use a photo of
					<a href="images/HouseOutdoor1.JPG">my old house</a> as a logo
					(which I took with my first digital camera). (People nagged
					me to change it to a photo of my caravan, where I used to live. And
					so I have on my travel pages. :-)</p>

				<p>A FavIcon, or Favorite Icon, is the small icon in the Address
					field of your browser and in browser's bookmarks/favorites. My
					FavIcon is based on my logo (om English pages, i.e. the sunset)
					and I used <strong>HTML-Kit's</strong> online service to create mine.</p>

