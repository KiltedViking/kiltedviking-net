				<h3 class="mt-4">About me</h3>
				<p>My name is Bj&ouml;rn (<em>bear</em> in Swedish), or
					&quot;Bj&quot; for short, and I presently live in Sweden.
					The name 'Kilted Viking' was coined by a cousin and refers to
					my Swedish-Scottish ancestory and me owning a kilt (Grant tartan).
					I was born and raised in <strong>B&aring;stad</strong> (southern Sweden), spending most
					of my summers visiting relatives in Scotland (mainly Pitlochry
					and Cupar).</p>

				<p>In 1995 I moved to <strong>Eskilstuna</strong> (an hour west of Stockholm, or
					the other - west - end of lake M&auml;laren) for studies and later
					work (until mid 2006). I have also lived in Halmstad and V&auml;xj&ouml; for studied
					as well, the former also for work.</p>
					
				<p>After a year off work, living in a caravan and travelling, and a 
					year in V&auml;ster&aring;s (late 2007 to 2008), I moved to 
					<strong>Stockholm</strong> in late 2008 - first to
					<strong>Bromma</strong>, then <strong>Huddinge</strong>, and
					lastly <strong>Saltsj&ouml;baden (Nacka)</strong>. I moved here 
					to work for <strong>Ecolabelling Sweden</strong>.</p>

